# Python code to demonstrate the working of 
# tan() and hypot() 

# importing "math" for mathematical operations 
import math 

a = math.pi/6
b = 3
c = 4

# returning the value of tangent of pi/6 
print ("The value of tangent of pi/6 is : ", end="") 
print (math.tan(a)) 

